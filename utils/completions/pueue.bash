    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${i}" in
            pueue)
                cmd="pueue"
                ;;
            
            add)
                cmd+="__add"
                ;;
            clean)
                cmd+="__clean"
                ;;
            edit)
                cmd+="__edit"
                ;;
            enqueue)
                cmd+="__enqueue"
                ;;
            help)
                cmd+="__help"
                ;;
            kill)
                cmd+="__kill"
                ;;
            log)
                cmd+="__log"
                ;;
            parallel)
                cmd+="__parallel"
                ;;
